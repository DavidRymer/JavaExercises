package builderPattern;

public class Bicycle {
	
	public String make;
	public String model;
	public String frame;
	public String forks;
	public String handlebars;
	public String type;
	public String brakes;
	
	public Bicycle(
			
	String newMake,
	String newModel,
	String newFrame,
	String newForks,
	String newHandlebars,
	String newType,
	String newBrakes) {

	this.make = newMake;
	this.model = newModel;
	this.frame = newFrame;
	this.forks = newForks;
	this.handlebars = newHandlebars;
	this.type = newType;
	this.brakes = newBrakes;
	
	}
	
	
	
	

}
